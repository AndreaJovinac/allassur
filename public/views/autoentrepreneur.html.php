Toute personne qui souscrit un crédit afin de réaliser un projet immobilier, devra également souscrire une assurance de prêt. 
Cette dernière est une garantie pour la banque (ou autres organismes de prêts immobiliers) de continuer 
à percevoir le remboursement du crédit en cas de décès de l'emprunteur, d'une maladie invalidante, etc. 
Il existe aujourd'hui sur le marché de très nombreux types de contrat d'assurance, avec des garanties et des modalités diverses. 
Il n'est donc pas toujours facile de choisir ni de savoir laquelle est la plus intéressante en fonction de ses besoins. 
C’est là que le courtier en assurance de prêt peut jouer un rôle primordial. 

